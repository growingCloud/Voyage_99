# 숫자 집합 candidates 조합하여 합이 target이 되는 원소는 나열하라. 각 원소는 중복으로 나열 가능하다.
# 예제 1.
# 입력 >> candidates = [2, 3, 6, 7], target = 7
# 출력 >> [[7], [2, 2, 3]]
# 예제 2.
# 입력 >> candidates = [2, 3, 5], target = 8
# 출력 >> [[2, 2, 2, 2], [2, 3, 3], [3, 5]]